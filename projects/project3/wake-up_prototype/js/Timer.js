// Timer
//
// A class to define how a timer behaves

// Timer constructor
//
// Sets the properties with the provided arguments
function Timer(x,y,font,size,time,speed,title,font) {
  this.x = x;
  this.y = y;
  this.font = font;
  this.size = size;
  this.time = time;
  this.speed = speed;
  this.title = title;
  this.font = font;
}
